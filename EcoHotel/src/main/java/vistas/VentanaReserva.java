    private void bttRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttRegistrarActionPerformed
        // TODO add your handling code here:
        // Obtener datos del formulario
        String documento = txtDocumentoHuesped.getText().trim();
        String habitacionId = (String)comboHabitacion.getSelectedItem();
        String fechaIngreso = txtFechaIngreso.getText().trim();
        String fechaSalida = txtFechaSalida.getText().trim();

        // Validar que los campos no esten vacios
        if (documento.isEmpty() || habitacionId == null || fechaIngreso.isEmpty() || fechaSalida.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }

        try {
            // Validar formato de fechas y convertir a LocalDate
            LocalDate entrada = LocalDate.parse(fechaIngreso);
            LocalDate salida = LocalDate.parse(fechaSalida);

            // Obtener huesped y habitacion registrados 
            HuespedDTO huesped = hControlador.buscarHuesped(documento);
            HabitacionDTO habitacion = habControlador.buscarHabitacion(habitacionId);

            // Verificar que existan los datos
            if (huesped == null || habitacion == null) {
                JOptionPane.showMessageDialog(this, "Huesped o habitacion no encontrados.");
                return;
            }

            // Registrar la reserva
            rControlador.registrarReserva(documento + "-" + habitacionId, entrada, salida, huesped, habitacion);
                JOptionPane.showMessageDialog(this, "Reserva registrada.");
        limpiarCamposReserva();
        cargarTablaReservas(); 

    } catch (DateTimeParseException e) {
        // Manejo de error si las fechas no tienen el formato correcto
        JOptionPane.showMessageDialog(this, "Formato de fecha invalido. Usa YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        // Manejo de cualquier otro error
        JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_bttRegistrarActionPerformed

    private void bttBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttBusActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText().trim(); // Obtener el ID

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un ID para buscar");
        return;
    }

    ReservaDTO r = rControlador.buscarReserva(id);

    if (r != null) {
        txtID.setText(r.getHuesped().getDocumento());
        txtDocHues.setText(r.getHabitacion().getId());
        comboHabitacion.setSelectedItem(r.getHabitacion().getId());
        txtFecIn.setText(r.getFechaEntrada().toString());
        txtFecSa.setText(r.getFechaSalida().toString());
    } else {
        JOptionPane.showMessageDialog(this, "Reserva no encontrada");
    }
    }//GEN-LAST:event_bttBusActionPerformed

    private void bttEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEliminarActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText().trim();

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el ID de la reserva a eliminar");
            return;
        }

        boolean eliminado = rControlador.eliminarReserva(id);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Reserva eliminada");
            cargarTablaReservas(); // Refrescar la tabla
            limpiarCamposReserva();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar");
        }
    }//GEN-LAST:event_bttEliminarActionPerformed

    private void bttEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditarActionPerformed
        // TODO add your handling code here:
        String documento = txtDocumentoHuesped.getText().trim();
        String fechaIngresoTexto = txtFechaIngreso.getText().trim();
        String fechaSalidaTexto = txtFechaSalida.getText().trim();
        String idHabitacion = (String) comboHabitacion.getSelectedItem();

        if (documento.isEmpty() || fechaIngresoTexto.isEmpty() || fechaSalidaTexto.isEmpty() || idHabitacion == null) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }

        try {
            LocalDate fechaIngreso = LocalDate.parse(fechaIngresoTexto);
            LocalDate fechaSalida = LocalDate.parse(fechaSalidaTexto);
        
            rControlador.editarReserva(documento, fechaSalida, fechaSalida);
            JOptionPane.showMessageDialog(this, "Reserva actualizada.");
            cargarTablaReservas();
            limpiarCamposReserva();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bttEditarActionPerformed

