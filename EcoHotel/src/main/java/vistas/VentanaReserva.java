    private void cargarTablaReservas() {
    DefaultTableModel modelo = (DefaultTableModel) tabla.getModel(); // Obtener el modelo de la tabla
    modelo.setRowCount(0); // Limpiar todas las filas actuales

    ArrayList<ReservaDTO> lista = rControlador.obtenerTodasLasReservas(); // Obtener reservas 

    for (ReservaDTO r : lista) {
        Object[] fila = {
            r.getId(),
            r.getHuesped().getDocumento(),
            r.getHabitacion().getId(),
            r.getFechaEntrada().toString(),
            r.getFechaSalida().toString()
        };
        modelo.addRow(fila); // Agregar la fila al modelo
    }
}

    
    private void cargarHabitacionesDisponibles() {
    comboHabitacion.removeAllItems();
    ArrayList<HabitacionDTO> habitaciones = habControlador.obtenerTodasLasHabitaciones();
    for (HabitacionDTO hab : habitaciones) {
        comboHabitacion.addItem(hab.getId());
    }
}
    
